// Example String Data Type with concatenation

let str1: string = "Hello";
let str2: string = "World";
let concatenatedString: string = str1 + " " + str2;
console.log("Concatenated String:", concatenatedString);

// Example Number Data Type with arithmetic operations like add, subtraction, multiplication, etc

let num1: number = 10;
let num2: number = 5;
let addition: number = num1 + num2;
let subtraction: number = num1 - num2;
let multiplication: number = num1 * num2;
let division: number = num1 / num2;
console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);

// Example Boolean Data Type

let isTrue: boolean = true;
let isFalse: boolean = false;
console.log("isTrue:", isTrue);
console.log("isFalse:", isFalse);

// Example null Data Type

let nullValue: null = null;
console.log("Null Value:", nullValue);

// Example undefined Data Type

let undefinedValue: undefined = undefined;
console.log("Undefined Value:", undefinedValue);

// Example symbol Data Type

let sym1: symbol = Symbol("key1");
let sym2: symbol = Symbol("key2");
console.log("Symbol 1:", sym1);
console.log("Symbol 2:", sym2);

// Example bigint Data Type

let bigIntValue: bigint = 9007199254740991n;
console.log("Big Int Value:", bigIntValue);
